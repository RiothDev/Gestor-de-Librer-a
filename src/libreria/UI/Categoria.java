    private void jList1ValueChanged(javax.swing.event.ListSelectionEvent evt) {//GEN-FIRST:event_jList1ValueChanged

        try {
            if(jList1.getSelectedValue() != null) {
                String num = jList1.getSelectedValue().split(" - ")[1];
            
                PreparedStatement statement = model.getConnection().prepareStatement("SELECT * FROM categorias WHERE numCategoria = ?");
                statement.setString(1, num);
            
                ResultSet result = statement.executeQuery();
            
                while(result.next()) {
                    jTextField1.setText(result.getString("categoria"));
                    jTextField2.setText(result.getString("numCategoria"));
                    jTextField3.setText(result.getString("id"));
                }  
            }
            
        } catch(SQLException e) {
            JOptionPane.showMessageDialog(null, "Error al intentar conseguir la categoría.");
        }
    }//GEN-LAST:event_jList1ValueChanged

