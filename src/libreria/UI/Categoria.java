    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed

        try {
            if(validateForm() && validateInsert(jTextField2.getText())) {
                PreparedStatement statement = model.getConnection().prepareStatement("INSERT INTO categorias (categoria, numCategoria) VALUES (?, ?)");
                statement.setString(1, jTextField1.getText());
                statement.setString(2, jTextField2.getText());
            
                statement.executeUpdate();
            
                clear();
                render();
            } else {
                JOptionPane.showMessageDialog(null, "Error al intentar crear la categoría, tal vez los parámetros no sean correctos.");
            }
            
        } catch(SQLException e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(null, "Error al intentar crear la categoría.");
        }
    }//GEN-LAST:event_jButton1ActionPerformed

