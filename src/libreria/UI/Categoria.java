    public boolean validateForm() {
        return !(jTextField1.getText().equals("") || jTextField1.getText().equals(""));
    }
    
    public boolean validateInsert(String numCategoria) {
        try {
            PreparedStatement statement = model.getConnection().prepareStatement("SELECT * FROM categorias WHERE numCategoria = ?");
            statement.setString(1, numCategoria);
            
            ResultSet result = statement.executeQuery();
            
            return !(result.next());
            
        } catch(SQLException e) {
            return false;
        }
    }
    
    public void render() {
        try {
            PreparedStatement statement = model.getConnection().prepareStatement("SELECT * FROM categorias");
            ResultSet result = statement.executeQuery();
            
            DefaultListModel JLIST = new DefaultListModel();
            
            while(result.next()) {
                JLIST.addElement(result.getString("categoria") + " - " + Integer.toString(result.getInt("numCategoria")));
            }
            
            jList1.setModel(JLIST);
            
        } catch(SQLException e) {
            JOptionPane.showMessageDialog(null, "Error al intentar conseguir las categorías de la base de datos.");
        }
    }
    
    public void clear() {
        jTextField1.setText("");
        jTextField2.setText("");
        jTextField3.setText("");
    }
    
    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed

        try {
            if(validateForm()) {
                PreparedStatement statement = model.getConnection().prepareStatement("DELETE FROM categorias WHERE id = ?");
                statement.setString(1, jTextField3.getText());
            
                statement.executeUpdate();
            
                clear();
                render();   
            }
            
        } catch(SQLException e) {
            JOptionPane.showMessageDialog(null, "Error al intentar remover la categoría de la base de datos.");
        }
    }//GEN-LAST:event_jButton2ActionPerformed

